package com.doom.s1.storeList.tag;

public class StoreTagVO {
	
	private long tag_key;
	private long st_key;
	private String tag_name;
	
	
	public long getTag_key() {
		return tag_key;
	}
	public void setTag_key(long tag_key) {
		this.tag_key = tag_key;
	}
	public long getSt_key() {
		return st_key;
	}
	public void setSt_key(long st_key) {
		this.st_key = st_key;
	}
	public String getTag_name() {
		return tag_name;
	}
	public void setTag_name(String tag_name) {
		this.tag_name = tag_name;
	}	

}
