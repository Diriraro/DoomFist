package com.doom.s1.storeList.reviewFile;

public class ReviewFileVO {

	private long re_num;
	private long refile_key;
	private String refile_name;
	private String refile_oriname;
	
	
	public long getRe_num() {
		return re_num;
	}
	public void setRe_num(long re_num) {
		this.re_num = re_num;
	}
	public long getRefile_key() {
		return refile_key;
	}
	public void setRefile_key(long refile_key) {
		this.refile_key = refile_key;
	}
	public String getRefile_name() {
		return refile_name;
	}
	public void setRefile_name(String refile_name) {
		this.refile_name = refile_name;
	}
	public String getRefile_oriname() {
		return refile_oriname;
	}
	public void setRefile_oriname(String refile_oriname) {
		this.refile_oriname = refile_oriname;
	}
	
}
